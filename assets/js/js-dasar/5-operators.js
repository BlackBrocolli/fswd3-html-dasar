// Aritmethic operator
let a = 10;
let b = 3;

console.log("Operator Aritmatika")
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);
console.log("");

// Assignment Operator
console.log("Operator Penugasan");
let c = 2;
console.log(c);

c += 5;
console.log(c);
console.log("");

// Comparison Operator
let d = 4;
let e = 5;

console.log("Operator Perbandingan");
console.log(d == e)
console.log(d < e)
console.log(d > e)
console.log(d != e)
console.log("")

// Logical Operator
let f = true;
let g = false;

console.log("Operator Logika")
console.log(f && g)
console.log(f || g)
console.log(!f)
console.log("")

// Bitwise Operator
let h = 00111100;
let i = 00001101;

console.log("Operator Bitwise")
console.log(h & i)
console.log(h | i)
console.log(h ^ i)
console.log(~h)
console.log("")

// Ternary Operator
let j = 1
let k = 1

console.log("Ternary Operator")
console.log(j == k ? "yes" : "no")