// Basic Function
function salam() {
    console.log("Hello World");
}

salam();

// Function Expression
let salamLagi = function() {
    console.log("Hello Gais")
}

salamLagi();

// Arrow Function
var salamTerus = () => {
    console.log("Hello Teman")
}

var salamSalaman = () => console.log("Salam-salaman")

salamTerus()
salamSalaman()